   TITLE   EXIMM    (EXE) Пример непосредственных операндов
   ; (Кодируется для ассемблирования, но не для выполнения)
   DATASG  SEGMENT   PARA  'Data'
   FLD1    DB        ?
   FLD2    DW        ?
   DATASG  ENDS
   CODESG  SEGMENT   PARA  'Code'
   BEGIN   PROC      FAR
           ASSUME    CS:CODESG,DS:DATAG

           ; Операции  пересылки  и  сравнения:
           :----------------------------------------------
           MOV       BX,275      ;Пересылка
           CMP       AL,H        ;Сравнение

           ; Арифметические  операции:
           ;----------------------------------------------
           ADS       AL,5        ;Сложение с переносом
           ADD       BH,12       ;Сложение
           SBB       AL,5        ;Вычитание с заемом
           SUB       FLD1,5      ;Вычитание

           ; Ротация и сдвиг на  один  бит
           ;----------------------------------------------
           RCL       BL,1        ; Ротация влево с переносом
           RCR       AH,1        ; Ротация вправо с переносом
           ROL       FID2,1      ; Ротация влево
           ROR       AL,1        ; Ротация вправо
           SAL       CX,1        ; Сдвиг влево
           SAR       BX,1        ; Арифметич. сдвиг вправо
           SHR       FLD1,1      ; Сдвиг вправо

           ; Логические операции:
           ;----------------------------------------------
           AND       AL,00101100B   ; AND (РЕГИСТР)
           OR        BH,2AH         ; OR (РЕГИСТР)
           TEST      BH.7AH         ; TEST (РЕГИСТР)
           OR        FLD1,23H       ; XOR (ПАМЯТЬ)
  BEGIN    ENDP

  CODESG   ENDS
           END

